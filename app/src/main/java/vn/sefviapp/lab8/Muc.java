package vn.sefviapp.lab8;

public class Muc {
    int hinh;
    String chuoi;

    public Muc(int hinh, String chuoi) {
        this.hinh = hinh;
        this.chuoi = chuoi;
    }

    public int getHinh() {
        return hinh;
    }

    public void setHinh(int hinh) {
        this.hinh = hinh;
    }

    public String getChuoi() {
        return chuoi;
    }

    public void setChuoi(String chuoi) {
        this.chuoi = chuoi;
    }
}
